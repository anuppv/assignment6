function namevalueof(){
    console.log("First example");
    let name = ['Sandeep','Ganga','Amar','Ravi','Kumar'];

    console.log("The list of names are:",name);
    console.log("The valueof result is:",name.valueOf());
}
function numvalueof() {
    console.log("Second example");
    let num = [17,3,25,9,36];
    
    console.log("The list of numbers are:",num);
    console.log("The valueof result is:",num.valueOf());
}
namevalueof();
numvalueof();